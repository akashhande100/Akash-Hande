package patternPrograms;

public class NumberPattern {
	
	public static void main(String[] args) {
		
		int a = 1;
		
	//	char ch = 'a';
		
		for (int i = 0 ; i < 5 ; i++) {
			
			for (int j = 0 ; j <=i ; j++) {
				
				System.out.print(a);
			}
			
			System.out.println();
			a++;
			
			
		}
	}

}
